public class DataTypes {
    public static void main(String[] args) {
        // Integer
        byte b = 127;
        System.out.println("Byte usage: " + (++b));
        short s = 32767;
        System.out.println("Short usage: " + (++s));
        int i = 32767;
        System.out.println("Int usage: " + (++i));
        long l = 32676L;
        System.out.println("Long usage: " + (++l));


        //floating point data type
        float f = 1.1f;
        System.out.println("Float usage: " + f);

        final double d = 3.14;
        System.out.println("Double usage: " +d);

        // Character data type
        char c = 1001;
        System.out.println("Char usage: " +c);

        // Boolean data type
        boolean bool = true;
        System.out.println("Boolean usage:" +bool);

    }
}
